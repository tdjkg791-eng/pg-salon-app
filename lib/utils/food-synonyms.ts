/**
 * キーワードに対応する同義語リストを返す。大文字小文字・前後空白は無視。
 * 部分一致は行わず、辞書キーに対する完全一致のみ。
 */
export function getSynonyms(keyword: string): string[] {
  const key = keyword.trim();
  return FOOD_SYNONYMS[key] ?? [];
}

/**
 * キーワードに対応するカテゴリを返す。辞書キー完全一致のみ。
 */
export function getCategoryHint(keyword: string): string | null {
  const key = keyword.trim();
  return CATEGORY_HINTS[key] ?? null;
}
